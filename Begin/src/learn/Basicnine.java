package learn;

public class Basicnine {
	public static void main (String[]args) {
		int day=20;
		switch(day) {
		case 1:
			System.out.println("monday");
			break;
		case 2:
			System.out.println("tuesday");
			break;
		case 3:
			System.out.println("wednesday");
			break;
		default:
			System.out.println("no day");
			
		}
		
	}
}
